package com.algorithms.minpathsum;

public class MinPathSum {

    public int minPathSum(int[][] grid) {
        //获取二维数组 grid 的行数
        int m = grid.length;
        //获取二维数组 grid 第一行的列数
        int n = grid[0].length;
        int[][] dp = new int[m][n];

        dp[0][0] = grid[0][0];

        // 第一列：只能从上方来
        for (int i = 1; i < m; i++) {
            dp[i][0] = dp[i - 1][0] + grid[i][0];
        }

        // 第一行：只能从左方来
        for (int j = 1; j < n; j++) {
            dp[0][j] = dp[0][j - 1] + grid[0][j];
        }

        // 其余格子：取上方和左方的较小值
        for (int i = 1; i < m; i++) {
            for (int j = 1; j < n; j++) {
                dp[i][j] = grid[i][j] + Math.min(dp[i - 1][j], dp[i][j - 1]);
            }
        }

        return dp[m - 1][n - 1];
    }
}
/**
 * ⏺ 思路：动态规划
 *
 *   dp[i][j] = 到达 (i,j) 的最小路径和
 *
 *   dp[i][j] = grid[i][j] + min(dp[i-1][j], dp[i][j-1])
 *
 *   边界：第一行只能从左来，第一列只能从上来。
 *
 * ⏺ 7 个测试全部通过。
 *
 *   ---
 *   DP 推导过程（示例矩阵）
 *
 *   grid:          dp:
 *   1  3  1        1   4   5
 *   1  5  1   →    2   7   6
 *   4  2  1        6   8   7  ← 答案
 *
 *   每格填入 grid[i][j] + min(上, 左)，右下角即为最小路径和 7。
 *
 *   复杂度： 时间 O(m×n)，空间 O(m×n)（可压缩为 O(n) 滚动数组）
 *  最小路径和 —— 图解
 *
 *   规则： 只能 →右 或 ↓下，求从左上到右下的数字之和最小。
 *
 *   ---
 *   第一步：想清楚「到达每个格子的最小代价」
 *
 *   到达任意格子 (i,j)，只有两条来路：
 *   - 从上面 (i-1, j) 走下来
 *   - 从左边 (i, j-1) 走过来
 *
 *   所以：
 *   到达 (i,j) 的最小代价 = 当前格子的值 + min(从上来的代价, 从左来的代价)
 *
 *   ---
 *   第二步：处理边界
 *
 *   - 第一行：左边没有格子，只能一路向右，代价逐格累加
 *   - 第一列：上面没有格子，只能一路向下，代价逐格累加
 *
 *   ---
 *   第三步：逐格填表（以示例为例）
 *
 *   原始 grid：
 *   1  3  1
 *   1  5  1
 *   4  2  1
 *
 *   填第一行（只能向右累加）：
 *   1  1+3=4  4+1=5
 *
 *   填第一列（只能向下累加）：
 *   1
 *   1+1=2
 *   2+4=6
 *
 *   填其余格子（取上、左中较小的，加上自身）：
 *   dp[1][1] = 5 + min(4, 2) = 5+2 = 7
 *   dp[1][2] = 1 + min(5, 7) = 1+5 = 6
 *   dp[2][1] = 2 + min(7, 6) = 2+6 = 8
 *   dp[2][2] = 1 + min(6, 8) = 1+6 = 7  ← 答案
 *
 *   完整 dp 表：
 *   1   4   5
 *   2   7   6
 *   6   8   7
 *
 *   右下角 7 就是答案，对应路径：
 *   (0,0)→(0,1)→(0,2)→(1,2)→(2,2)
 *     1  →  3  →  1  →  1  →  1   = 7
 *
 *   ---
 *   对应代码
 *
 *   // 第一列累加
 *   for (int i = 1; i < m; i++)
 *       dp[i][0] = dp[i-1][0] + grid[i][0];
 *
 *   // 第一行累加
 *   for (int j = 1; j < n; j++)
 *       dp[0][j] = dp[0][j-1] + grid[0][j];
 *
 *   // 其余：上 vs 左 取小的
 *   for (int i = 1; i < m; i++)
 *       for (int j = 1; j < n; j++)
 *           dp[i][j] = grid[i][j] + Math.min(dp[i-1][j], dp[i][j-1]);
 *
 *   核心思想：不需要枚举所有路径，每个格子只需看两个邻居，把「全局最优」拆成「局部最优」逐步推导，这就是动态规划。
 * **/